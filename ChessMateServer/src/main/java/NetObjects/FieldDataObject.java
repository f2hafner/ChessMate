package NetObjects;

public class FieldDataObject {
    int x;
    int y;
    public FieldDataObject(){}

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
